package models;

public abstract class GalacticID {
	
	/////////////////////////////////////////////////////////
	// Instance variables
	private long idNum;
	
	/////////////////////////////////////////////////////////
	// Static/class variable
	private static long nextIdNum;
	
	/////////////////////////////////////////////////////////
	// Default Constructor
	public GalacticID() {
		idNum = nextIdNum++;
	}
	
	/////////////////////////////////////////////////////////
	// Getters
	public long getIdNum() { return idNum; }
	
	/////////////////////////////////////////////////////////
	// Get the empire ID
	public abstract String getEmpireIdStr();
}





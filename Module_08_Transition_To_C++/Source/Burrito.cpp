#include "../Headers/Burrito.h"

///////////////////////////////////////////////////////////////////////////////////
// Constructor
///////////////////////////////////////////////////////////////////////////////////
Burrito::Burrito(string meat, string beans)
{
	meatType = meat;
	beanType = beans;
}

///////////////////////////////////////////////////////////////////////////////////
// Deconstructor
///////////////////////////////////////////////////////////////////////////////////
Burrito::~Burrito() { }

string Burrito::getSummary() {
	string summary = "Burrito w/ " + meatType + " & " + beanType;
	return summary;
}

